<template>
  <el-select @change="change" v-model="val" :placeholder="placeholder" v-bind="$attrs">
    <slot name="head"></slot>
    <template v-for="item in types">
      <!-- 将 `item` 对象作为一个插槽的 prop 传入。-->
      <slot v-bind:item="item">
        <!-- 回退的内容 -->
        <el-option :key="item.id" :label="item.dictName" :value="item.id"></el-option>
      </slot>
    </template>
    <slot name="foot"></slot>
  </el-select>
</template>
<script>
import { getSelectType } from '@/api/common'
import { mapGetters } from 'vuex'

/**
 * 可选的type值
network_type	网点类型
manage_type	经营类型
driving_type	驾驶证类型
department_type	部门类型
upType	upRemark
payment_type	中转付款方式
department_type	部门类型
menu_type	菜单类型
network_status	网点状态
ship_time_rule	开单时间规则
notify_cargo_rule	通知放货规则
load_type	配载类型
apportion_type	分摊方式
short_batch_type	短驳批次状态
main_batch_type	干线批次状态
delivery_batch_type	送货批次状态
ship_status	运单状态
ship_delivery_method	交接方式
ship_pay_way	付款方式
ship_receipt_require	回单要求
ship_shipping_type	运输方式
ship_business_type	业务类型
ship_effective	时效性
sign_cocument_type	签收证件
sign_type	签收类型
ship_pay_way	付款方式
rec_status	回单回收状态
send_status	回单寄出状态
accept_status	回单接收状态
giveout_status	回单发放状态
rec_type	回单回收情况
accept_type	回单接收情况
abnormal_status	异常状态
abnormal_type	异常类型
truck_source	车辆来源
truck_type	车型
ship_other	其他
type	备注
department_type	部门类型
order_status 订单状态
 */

export default {
  props: {
    orgid: {
      type: [Number, String],
      default: ''
    },
    type: {
      type: String,
      default: '',
      required: true
    },
    placeholder: {
      type: String,
      default: '请选择'
    },
    value: {
      type: [Number, String, Array]
    }
  },
  computed: {
      ...mapGetters([
          'otherinfo'
      ])
  },
  data () {
    return {
      val: '',
      types: []
    }
  },
  watch: {
    value (newVal) {
      this.val = newVal
    }
  },
  mounted () {
    this.val = this.value
    getSelectType(this.type, this.orgid || this.otherinfo.companyId).then(data => {
      this.types = data
    })
  },
  methods: {
    change () {
      this.$emit('input', this.val)
    }
  }
}
</script>
