import SearchForm from './components/search'
import {postDeliveryList,postCancelSign } from '@/api/operation/sign'
import { mapGetters } from 'vuex'
import Pager from '@/components/Pagination/index'
// import TableSetup from './components/tableSetup'
import Addsign from './components/add'
import Addbatch from './components/batch'
export default {
    components: {
        SearchForm,
        Addsign,
        Addbatch,
        // TableSetup,
        Pager
    },
    computed: {
        ...mapGetters([
            'otherinfo'
        ]),
        orgid () {
            // console.log(this.selectInfo.orgid , this.searchQuery.vo.orgid , this.otherinfo.orgid)
            return this.isModify ? this.selectInfo.orgid : this.searchQuery.vo.orgid || this.otherinfo.orgid
        }
    },
    mounted () {
        // this.searchQuery.vo.orgId = this.otherinfo.orgid
        Promise.all([this.fetchAllreceipt(this.otherinfo.orgid)]).then(resArr => {
            this.loading = false
            // this.licenseTypes = resArr[1]
          })
        },
        data() {
            return {
                btnsize: 'mini',
                component: 'Send',
                selectInfo: {},
                selected:[],
                dataset:[],
                AddSignVisible:false,
                AddBatchVisible:false,
                setupTableVisible: false,
                popVisible:false,
                isModify: false,
                isDelivery:false,
                isPick:false,
                isSongh:false,
                dotInfo: [],
                repertoryId:'',
                signId:'',
                // loading:false,
                searchQuery: {
                  "currentPage":1,
                  "pageSize":10000,
                  "vo":{
                    "shipId":'',
	                  "signId":''
                  }
                },
                total: 0,
                id:''
            }
        },
        methods: {
        fetchAllreceipt() {
            this.loading = true
            return postDeliveryList(this.searchQuery).then(data => {
                this.dataset = data.list
                this.total = data.total
                this.signId = data.signId
            })
        },
        fetchData () {
          this.fetchAllreceipt()
        },
         // 获取组件返回的搜索参数
        getSearchParam (searchParam) {
            // 根据搜索参数请求后台获取数据
            Object.assign(this.searchQuery.vo, searchParam)
            //this.searchQuery.vo.orgId = searchParam.orgid
            this.fetchData()
        },
        handlePageChange (obj) {
            this.searchQuery.currentPage = obj.pageNum
            this.searchQuery.pageSize = obj.pageSize
        },
        // getSearchParam (searchParam) {
        //   Object.assign(this.searchQuery.vo, searchParam)
        //   this.fetchAllreceipt()
        // },
        doAction (type) {
          if(type==='import'){
            this.showImport()
            return false
          }
          // 判断是否有选中项
          if(!this.selected.length ){
              this.$message({
                  message: '请选择要操作的项~',
                  type: 'warning'
              })
               return false
          }

          switch (type) {
              //签收
            case 'pick':
              let ids = this.selected
           
              if(ids.length > 1 ){
               /*  this.searchQuery.vo.repertoryId = ids.map(el => {
                  return el.repertoryId
                }) */
                this.dotInfo = ids
        
                this.isSongh = true
              
                this.isDelivery = true
                this.isPick = false
                this.openAddBatch()
              }else{
                this.repertoryId = this.selected[0]
                 this.isDelivery = true
                 this.isPick = false
                 
                this.openAddSign()
              }
            break;
            case 'amend': 
              
              if(this.selected.length > 1){
                  this.$message({
                      message: '每次只能修改单条数据',
                      type: 'warning'
                  })
                }else{
                  this.isPick = true
                  this.isDelivery = false
                  this.repertoryId = this.selected[0]
                  this.id = this.selected[0].signId
                  console.log(this.id);
                  this.openAddSign()
                }
              break;
             
            case 'cancel':
              let shipId = this.selected[0].shipId
              let signId = this.selected[0].signId
          
              this.searchQuery.vo.shipId = shipId
              this.searchQuery.vo.signId = signId
                // console.log(repertoryId);
              postCancelSign(this.searchQuery.vo).then(res=>{
                this.$message({
                  message: '取消签收成功~',
                  type: 'success'
                })
                  this.fetchAllreceipt()
                  return false
                }).catch(err => {
                  this.$message.error(err)
                  // this.closeAddDot()
                })
              break;
            }
          // 清除选中状态，避免影响下个操作
          this.$refs.multipleTable.clearSelection()
        },
        setTable () {
          this.setupTableVisible = true
        },
        closeSetupTable () {
          this.setupTableVisible = false
        },
        openAddSign () {
          this.AddSignVisible = true
        },
        closeAddSign () {
          this.AddSignVisible = false
        },
        openAddBatch () {
          this.popVisible = true
   
        },
        closeAddBacth () {
      
           this.popVisible = false;
        },
        clickDetails(row, event, column){
          this.$refs.multipleTable.toggleRowSelection(row)
        },
        getSelection(selected){
          this.selected = selected
        }

    }
}
</script>
